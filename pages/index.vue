  if (element) {
    const updatesText = element.innerText;
    const updates = updatesText.match(/V\d+\.\d+\.\d+/g); // 匹配所有版本号格式
    if (updates && updates.length) {
      version.value = updates.pop(); // 获取最后一个元素，即最新版本号
    }
  } else {
    console.warn('No update details found in the DOM.');
  }
}

